package hHocaForLoop;

public class S2 {

    public static void main(String[] args) {


        // 0-255 e kadar olan harflerin , sayı ve harf değerini konsola yazdırınız.

        for (int i = 0; i <=255 ; i++) {
            for (char j = 0; j <=255 ; j++) {

                System.out.println(i+" "+ j);

            }

        }

    }
}
